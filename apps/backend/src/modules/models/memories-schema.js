import mongoose, { Schema } from 'mongoose';

const memorySchema = new Schema(
  {
    userId: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'User',
      required: true,
    },
    title: {
      type: String,
      required: true,
      trim: true,
    },
    description: {
      type: String,
      required: true,
      trim: true,
    },
    location: {
      lat: {
        type: Number,
        required: true,
        min: -90,
        max: 90,
      },
      lng: {
        type: Number,
        required: true,
        min: -180,
        max: 180,
      },
    },
    photoURL: {
      type: String,
      default: null,
    },
  },
  { timestamps: true },
);

export const Memory = mongoose.model('Memory', memorySchema);
